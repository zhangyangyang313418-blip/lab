function cloneItemsForSteeringSide(
  items: EditableTestItem[],
  steeringSide: SteeringSide,
  standardName: string,
  noTestCodes: string[] = [],
): EditableTestItem[] {
  const noTestCodeSet = new Set(noTestCodes);

  return items
    .filter((item) => !noTestCodeSet.has(item.code))
    .map((item) => ({
      ...item,
      id: `${item.id}-${steeringSide.toLowerCase()}`,
      standard: item.standard.includes("环境可靠性测试大纲") ? standardName : item.standard,
      procedure: `${steeringSide} / ${item.procedure}`,
      tags: [...item.tags.filter((tag) => tag !== "LHD" && tag !== "RHD"), steeringSide],
    }));
}

